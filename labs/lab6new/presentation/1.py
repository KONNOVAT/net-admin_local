import networkx as nx
import matplotlib.pyplot as plt

# Создание графа
G = nx.DiGraph()

# Добавление узлов
G.add_node("Иванов Иван (Информатика)")
G.add_node("Петрова Анна (Математика)")
G.add_node("Сидоров Алексей (Физика)")
G.add_node("Алгоритмы")
G.add_node("Математический анализ")
G.add_node("Физика")
G.add_node("Петров И.С.")
G.add_node("Сидоров А.В.")
G.add_node("Кузнецова Е.Е.")

# Добавление связей
G.add_edges_from([
    ("Иванов Иван (Информатика)", "Алгоритмы"),
    ("Иванов Иван (Информатика)", "Математический анализ"),
    ("Петрова Анна (Математика)", "Математический анализ"),
    ("Сидоров Алексей (Физика)", "Физика"),
    ("Петров И.С.", "Алгоритмы"),
    ("Сидоров А.В.", "Математический анализ"),
    ("Кузнецова Е.Е.", "Физика"),
])

# Визуализация графа
pos = nx.spring_layout(G)
nx.draw(G, pos, with_labels=True, node_size=3000, node_color="lightblue", font_size=10, font_color="black")
plt.show()
